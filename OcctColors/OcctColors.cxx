void PrintColor(Quantity_NameOfColor color, const char *name)
{
    Quantity_Color col(color);
    double red = col.Red();
    double green = col.Green();
    double blue = col.Blue();
    //
    uint8_t r = (uint8_t)(red * 255);
    uint8_t g = (uint8_t)(green * 255);
    uint8_t b = (uint8_t)(blue * 255);
    // Format HTML color
    ostringstream ss;
    // ss << "#" << std::hex << std::setfill('0') << std::setw(2) << uppercase << (int)r
    //     << std::setfill('0') << std::setw(2) << uppercase << (int)g
    //     << std::setfill('0') << std::setw(2) << uppercase << (int)b;
    ss  << std::hex << std::setfill('0') << std::setw(2) << uppercase << (int)r
        << std::setfill('0') << std::setw(2) << uppercase << (int)g
        << std::setfill('0') << std::setw(2) << uppercase << (int)b;
    string htmlcolor = ss.str();
    
//         shades: [
//   ["FF0000", "Red"],
    cout<<"[\""<<htmlcolor<<"\", \""<<name<<"\"],"<<"\n";
    // Print table
    // cout << "<tr>\n";
    // cout << "<td><b>" << name << "</b></td>\n";
    // cout << "<td style=\"background-color: " << htmlcolor << "; min-width: 40px;\"></td>\n";
    // cout << "<td>" << htmlcolor << "</td>\n";
    // cout << "</tr>\n";
}

